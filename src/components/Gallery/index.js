class Gallery extends Component {
  state = {thumbnailId: 0}

  onClickThumbnail = id => {
    this.setState({thumbnailId: id})
  }

  render() {
    const {thumbnailId} = this.state

    const {imageUrl, imageAltText} = imagesList[thumbnailId]

    return (
      <div className="gallery-app-container">
        <div className="content-card">
          <img src={imageUrl} alt={imageAltText} className="final-image" />
          <h4 className="heading">Nature Photography</h4>
          <p className="description">Nature Photography by Rahul</p>
          <ul className="thumbnail-list-container">
            {imagesList.map(eachImageObject => (
              <ThumbnailItem
                key={eachImageObject.id}
                objectDetails={eachImageObject}
                isActive={thumbnailId === eachImageObject.id}
                thumbnailClicked={this.onClickThumbnail}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}
